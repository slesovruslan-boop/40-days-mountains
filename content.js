// Функция получения всех дней в плоском виде
function getAllDays() {
  return CONTENT.stages.flatMap(stage => 
    stage.days.map(day => ({
      ...day,
      stageName: stage.name,
      stageNumber: stage.stage
    }))
  );
}

// Функция получения конкретного дня
function getDay(dayNumber) {
  const allDays = getAllDays();
  return allDays.find(d => d.day === dayNumber);
}

